#include "ft_3dmath.h"

void			ft_mat4scale(t_mat4 *mat, t_vec3 scale)
{
	ft_mat4mult(mat, MAT4(
		(scale.x, 0.f, 0.f, 0.f),
		(0.f, scale.y, 0.f, 0.f),
		(0.f, 0.f, scale.z, 0.f),
		(0.f, 0.f, 0.f, 1.f)
	));
}
